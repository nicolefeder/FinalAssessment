package com.example.finalassessment;

public class Bird {

    public String birdName;
    public String userEmail;
    public int zipCode;
    public int sightingImportance;



    public Bird() {

    }

    public Bird(String birdName, String userEmail, int zipCode, int sightingImportance){
        this.birdName = birdName;
        this.userEmail = userEmail;
        this.zipCode = zipCode;
        this.sightingImportance = sightingImportance;
    }

}